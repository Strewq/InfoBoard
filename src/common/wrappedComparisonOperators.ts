export function EqualTo(a: any, b: any) {
	return a === b;
}

export function NotEqualTo(a: any, b: any) {
	return a !== b;
}

export function GreaterThan(a: any, b: any) {
	return a > b;
}

export function LessThan(a: any, b: any) {
	return a < b;
}

export function GreaterThanOrEqualTo(a: any, b: any) {
	return a >= b;
}

export function LessThanOrEqualTo(a: any, b: any) {
	return a <= b;
}